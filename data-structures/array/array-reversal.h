#ifndef ARRAY_REVERSAL_H
#define ARRAY_REVERSAL_H

/**
 * Prints an array of integers to the console.
 * 
 * @param arr - The array of integers to be printed.
 */
void printArray(int arr[], int size);

/**
 * Reverse an array of integers in place.
 * 
 * @param arr - The array of integers to be reversed.
 * @param size - The length of the array.
 */
void *reverseArray(int *arr, int size);

#endif